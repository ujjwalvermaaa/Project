				if (e.keyCode === 38) {
					count--;
					count = count <= 0 ? items.length : count;
					items[count - 1].dataset.highlight = items[count - 1] ? 'true' : 'false';
					if (items[count]) {
						items[count].dataset.highlight = 'false';
					}
